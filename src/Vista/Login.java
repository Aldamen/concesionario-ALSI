    public void validarAcceso(){
        int resultado=0;
        try {
                String doc = txtDoc.getText();
                String pass = String.valueOf(txtPass.getPassword());
                String nuevaContra = null;
                try {
                    nuevaContra = Hash.toHexString(getSHA(pass));
        //        System.out.println(nuevaContra);
                } catch (NoSuchAlgorithmException ex) {
                    Logger.getLogger(Interfaz_vendedor.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println(doc);
                System.out.println(pass);
                String sql= "SELECT * FROM vendedores where No_documento='" + doc+ "' and contra='"+nuevaContra+"' ";
                
                Statement st=con.createStatement();
                ResultSet rs = st.executeQuery(sql);
                
                if (rs.next()) {
                    resultado=1;
                    System.out.println(resultado);
                    if (resultado==1) {
                        Interfaz_concesionario ppal = new  Interfaz_concesionario();
                         ppal.setVisible(true);
                         dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Error en el acceso,vuelva a intentarlo");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Documento o contrase単a incorrectos.");
                    limpiar();
                }    
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error en el acceso,vuelva a intentarlo ..."+ e.getMessage());
            
        }
    }
